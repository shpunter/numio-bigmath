import { assertEquals } from "@std/assert/equals";
import { FDR } from "../fdr.ts";

Deno.test("[20, 20, 22, 24, 28, 28, 32, 32, 32, 35, 35, 40, 46, 79]", () => {
  const result = FDR([
    "20",
    "20",
    "22",
    "24",
    "28",
    "28",
    "32",
    "32",
    "32",
    "35",
    "35",
    "40",
    "46",
    "79",
  ]);

  assertEquals(result, { min: "20", max: "79", binsNum: "12", binWidth: "4.97895920019746064602" });
});

